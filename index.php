                function calculateTotal(productNumber) {
                    var quantity = document.getElementById('quantity' + productNumber).value;
                    var price = getProductPrice(productNumber);
                    var total = quantity * price;
                    document.getElementById('totalPrice' + productNumber).innerHTML = "Total Price: Rs" + total.toFixed(
                        2);
                }

                function getProductPrice(productNumber) {
                    switch (productNumber) {
                        case 1:
                            return 10;
                        case 2:
                            return 15;
                        case 3:
                            return 20;
                        case 4:
                            return 12;
                        case 5:
                            return 18;
                        case 6:
                            return 25;
                        default:
                            return 0;
                    }
                }

                function openWhatsApp(phone, product, quantity, price) {
                    if (quantity < 1) {
                        alert("Quantity must be at least 1");
                        return;
                    }
                    var total = quantity * price;
                    var message = "I'm interested in buying " + quantity + " '" + product + "'. Total Price: Rs " +
                        total.toFixed(2);
                    window.open("https://api.whatsapp.com/send?phone=" + phone + "&text=" + encodeURIComponent(
                        message));
                }
